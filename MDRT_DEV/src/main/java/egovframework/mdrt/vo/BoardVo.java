package egovframework.mdrt.vo;

import java.util.Date;

public class BoardVo {
	private String bordId;
	private String bordGubun;
	private String bordTit;
	private String bordCont;
	private String insUsr;
	private Date insDt;
	
	
	public String getBordId() {
		return bordId;
	}
	public void setBordId(String bordId) {
		this.bordId = bordId;
	}
	public String getBordGubun() {
		return bordGubun;
	}
	public void setBordGubun(String bordGubun) {
		this.bordGubun = bordGubun;
	}
	public String getBordTit() {
		return bordTit;
	}
	public void setBordTit(String bordTit) {
		this.bordTit = bordTit;
	}
	public String getBordCont() {
		return bordCont;
	}
	public void setBordCont(String bordCont) {
		this.bordCont = bordCont;
	}
	public String getInsUsr() {
		return insUsr;
	}
	public void setInsUsr(String insUsr) {
		this.insUsr = insUsr;
	}
	public Date getInsDt() {
		return insDt;
	}
	public void setInsDt(Date insDt) {
		this.insDt = insDt;
	}
	
	
	
	
	
}
